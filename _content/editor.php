            <h3>Pasting from Word Documents, Websites, and other misc sources.
            </h3>
            <p>Here are a few tips to help you copy content into the Editor CMS.</p>
            <p>Often when copying from documents or websites, your clipboard will also carry over any styling you have on the page. The editor will take what you have pasted and try to replicate it as per your Word doc/website. This will of course cause your pages to display incorrectly.</p>
            <p>Select a Tip, that most works for you.</p>
        </div>

        <!-- Tip 1 -->
        <div class="content" id="editor-panel2-6">
            <h3>Tip 1 - Pasting directly into the CMS.</h3>
            <p>Unless it is <a href="http://en.wikipedia.org/wiki/Text_editor" title="plain text wiki" target="_blank">plain text</a>, pasting directly into the CMS from a Word document or website will need some help to get it to display correctly.</p>
            <p>Pasting directly from websites/documents will often bring unwanted formatting and tags the editor is unable to determine.</p>
            <ol>
                <li>After copying your content/text from a website or document, select (usually a Description field) and paste (Ctrl+v/cmd+v) it into the description field.</li>
                <li>Once done, highlight everything you have copied in the editor and copy it (Ctrl+c/cmd+c).</li>
                <li>Click 'View Code' in the upper right corner of the editor, and paste back what you have copied from step 2.</li>
                <li>Next, click 'View Content'. You will see it has removed all formatting.  Now remove all of the text in that field, and paste again .</li>
                <li>This may seem a long winded way, but infact it is a quick way to keep your formatting; such as bullet points, paragraphs and other spacing.</li>
            </ol>
        </div>

        <!-- Tip 2 -->
        <div class="content" id="editor-panel2-2">
            <h3>Tip 2 - Pasting into Notepad, and then into the CMS.</h3>
            <p>Unless it is <a href="http://en.wikipedia.org/wiki/Text_editor" title="plain text wiki" target="_blank">plain text</a>, pasting directly into the CMS from a Word document or website will need some help to get it to display correctly.</p>
            <p>Pasting directly from websites/documents will often bring unwanted formatting and tags the editor is unable to determine.</p>
            <ul>
                <li>After copying your content/text from a website or document, open Notepad on your computer. (Note: Not Microsoft Word or any other application that formats your text)</li>
                <li>Paste in your content/text into notepad.</li>
                <li>Highlight and copy all of the text once selected, and past into your description field.</li>
            </ul>
        </div>

