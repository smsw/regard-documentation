        <div class="content" id="order-2">
            <h5>Note: This is found under the main admin navigation and is not part of publishing/editing products.</h5>

            <h3>Viewing and Processing Orders</h3>
            <ol>
                <li>
                    Navigate to Store > Settings<br>
                    <img src="/_content/images/updatepostage_settings.png" alt="product postage"/>
                </li>
                <li>Select Shipping Methods.</li>
                <li>Displayed here, is the postage options for products. Select 1-3 working days as an example.</li>
                <li>Inside, you will find the page 'Shipping Rules'</li>
                <li>To adjust these charges, under 'Options' select the red 'Edit' link.<br>
                    <img src="/_content/images/updatepostage_optionsedit.png" alt="product postage"/>
                </li>
                <li>Under the shipping rule you selected, you will be given option to enter the 'minimum order weight', and 'base rate'. These are the values you adjust for postage costs.</li>
                <li>Once finished, click 'Submit' and the rules will change.</li>
            </ol>
        </div>
