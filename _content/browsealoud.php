            <h3>How BrowseAloud will work for the user</h3>
            <ol>
                <li>Clicking the BrowseAloud icon will launch the 'What is BrowseAloud' page.</li>
                <li>This page will only launch once when the BrowseAloud icon speaker has been clicked. </li>
                <ul>
                    <li>It will store a cookie in the users browser remembering they have launched the 'What is BrowseAloud' page for the first time.
                    <li>Once that cookie is stored, the user will not be presented with the 'What is BrowseAloud page' again when clicking the BrowseAloud icon.  </li>
                    <li>The only time a user will be presented with the 'What is BrowseAloud' page is if they are a new user and have not clicked the BrowseAloud icon before, or they have cleared their cookies.</li>
                </ul>
            </ol>
        </div>

