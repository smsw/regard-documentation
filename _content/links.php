            <h3>Download Links</h3>
            <div class="panel callout radius">
                You can put a download link within the body of the copy.
            </div>
            <ol>
                <li>Type download pdf or something similar into the page body.</li>
                <li>Highlight that text so it can be a link. <br>
                    <img src="/_content/images/downloadlinks_highlight.png" alt="highlight text"/>
                </li>
                <li>Click the link icon
                    <ul>
                        <li>Add the direct download link to the pdf. (See 'Getting a direct download link' tab above). </li>
                    </ul>
                </li>
                <li>Click update link, and then submit. It will then appear on your page.</li>
            </ol>
        </div>
        <div class="content" id="editing-link-2">
            <h3>Finding a link to your upload</h3>
            <div class="panel callout radius">
                How do I generate the url link to the pdf?
            </div>
            <ol>
                <li>Content > Files > File manager . Click the Upload File button. <br>
                 <img src="/_content/images/downloadlinks_upload.png" alt="upload"/>
                </li>

                <li>Select the file to upload, You then get this:. <br>
                    <img src="/_content/images/downloadlinks_uploaddialog.png" alt="upload"/>
                </li>
                <li>You can click the X to close the dialog.
                    <ul>
                        <li>Your uploaded file will then appear in yellow. <br>
                        <img src="/_content/images/downloadlinks_fileyellow.png" alt="file yellow"/>
                        </li>
                    </ul>
                </li>
                <li>Where it says File Name, you can right click that and select 'Copy link address'<br>
                  <img src="/_content/images/downloadlinks_copylink.png" alt="copy link address"/>
                </li>
                <li>
                    That link is now on your clipboard, so you can paste that into your browser window or into the Link dialog in the cms.
                </li>
            </ol>
        </div>
