            <h3>Frequently asked questions</h3>
            Please troubleshoot through the following frequently asked questions before contacting us:

            <hr>

            <ol>
                <li>I am unable to login to the CMS backend. I keep landing on the login page.
                    <br><br>
                    <strong>Answer:</strong> Quit all browser applications, and re-open. Clearing CACHE, and Cookies upon re-opening. If your site is online and is working correctly as a guest, we ask that you contact us under technical support for us to investigate your issue.
                </li>
                <li>
                    The website appears to view differently on some machines and browsers. <br><br>
                    <strong>Answer:</strong> Quit all browser applications, and re-open. Clearing CACHE, and resetting the browser settings. If you are unsure, please contact your IT department on how to reset your browser. If your site is online and is working correctly as a guest, we ask that you contact us under Technical Support if you are still having an issue, for us to investigate your issue.
                </li>
                <li>
                    I have published an entry, but upon further viewing the formatting on the live site looks wrong.<br><br>
                    <strong>Answer:</strong> Make sure that you have not copy and pasted directly from Word, Excel, or any other program without pasting the copy into a plain text editor first.
                </li>

                <li>
                    I am unable to view/locate where forms are in the backend.<br><br>
                    <strong>Answer:</strong> Please navigate to Add-ons, Modules, Click 'Freeform pro', and at the top of the page, click +Add, this will add the forms menu to your admin navigation.
                </li>

                <li>
                    I am unable to view/locate where the navigation is in the backend.<br><br>
                    <strong>Answer:</strong> Please navigate to Add-ons, Modules, Click NavEE, and at the top of the page, click +Add, this will add the Navigation to your admin navigation.
                </li>

                <li>
                    I am unable to view/locate where the store is in the backend.<br><br>
                    <strong>Answer:</strong> Please navigate to Add-ons, Modules, Click Store, and at the top of the page, click +Add, this will add the Store to your admin navigation.
                </li>

                <li>
                    I am unable to publish/edit the homepage.<br><br>
                    <strong>Answer:</strong> It is likely you have insufficient permissions to carry out this task. If you are an Administrator and this happens, please contact us via Technical Support.
                </li>

                <li>
                    I am unable to publish/edit the homepage.<br><br>
                    <strong>Answer:</strong> It is likely you have insufficient permissions to carry out this task. If you are an Administrator and this happens, please contact us via Technical Support.
                </li>

                <li>
                    I receive an error about a file being too large to upload, or is un-writable.<br><br>
                    <strong>Answer:</strong> It is likely you have tried to upload a file to large for the server to handle. Please resize the image in an image editing program and try again. If you are still unable to, and have tried a different image please contact us via Technical Support.
                </li>
            </ol>
